package com.maxgrayer.jsonimporter.models;

public class Instruments {
    private int band;
    private int bass;
    private int keys;
    private int drums;
    private int guitar;
    private int vocals;
    private int proBass;
    private int proKeys;
    private int proGuitar;

    public int getBand() {
        return band;
    }

    public void setBand(final int band) {
        this.band = band;
    }

    public int getBass() {
        return bass;
    }

    public void setBass(final int bass) {
        this.bass = bass;
    }

    public int getKeys() {
        return keys;
    }

    public void setKeys(final int keys) {
        this.keys = keys;
    }

    public int getDrums() {
        return drums;
    }

    public void setDrums(final int drums) {
        this.drums = drums;
    }

    public int getGuitar() {
        return guitar;
    }

    public void setGuitar(final int guitar) {
        this.guitar = guitar;
    }

    public int getVocals() {
        return vocals;
    }

    public void setVocals(final int vocals) {
        this.vocals = vocals;
    }

    public int getProBass() {
        return proBass;
    }

    public void setProBass(final int proBass) {
        this.proBass = proBass;
    }

    public int getProKeys() {
        return proKeys;
    }

    public void setProKeys(final int proKeys) {
        this.proKeys = proKeys;
    }

    public int getProGuitar() {
        return proGuitar;
    }

    public void setProGuitar(final int proGuitar) {
        this.proGuitar = proGuitar;
    }

}
